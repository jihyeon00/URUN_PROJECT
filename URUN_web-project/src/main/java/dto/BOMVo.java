package dto;

public class BOMVo {
	int BOM_ID;
	int BOM_ITEM_ID;
	int BOM_MATERIAL_ID;
	int BOM_MATERIAL_Quantity;
	
	
	public int getBOM_ID() {
		return BOM_ID;
	}
	public void setBOM_ID(int bOM_ID) {
		BOM_ID = bOM_ID;
	}
	public int getBOM_ITEM_ID() {
		return BOM_ITEM_ID;
	}
	public void setBOM_ITEM_ID(int bOM_ITEM_ID) {
		BOM_ITEM_ID = bOM_ITEM_ID;
	}
	public int getBOM_MATERIAL_ID() {
		return BOM_MATERIAL_ID;
	}
	public void setBOM_MATERIAL_ID(int bOM_MATERIAL_ID) {
		BOM_MATERIAL_ID = bOM_MATERIAL_ID;
	}
	public int getBOM_MATERIAL_Quantity() {
		return BOM_MATERIAL_Quantity;
	}
	public void setBOM_MATERIAL_Quantity(int bOM_MATERIAL_Quantity) {
		BOM_MATERIAL_Quantity = bOM_MATERIAL_Quantity;
	}
}
