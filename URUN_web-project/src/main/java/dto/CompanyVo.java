package dto;

public class CompanyVo {
	int Company_Code;
	String Company_Name;
	String Company_Email;
	String Company_division;
	String Company_input_date;
	String Company_output_date;
	public int getCompany_Code() {
		return Company_Code;
	}
	public void setCompany_Code(int company_Code) {
		Company_Code = company_Code;
	}
	public String getCompany_Name() {
		return Company_Name;
	}
	public void setCompany_Name(String company_Name) {
		Company_Name = company_Name;
	}
	public String getCompany_Email() {
		return Company_Email;
	}
	public void setCompany_Email(String company_Email) {
		Company_Email = company_Email;
	}
	public String getCompany_division() {
		return Company_division;
	}
	public void setCompany_division(String company_division) {
		Company_division = company_division;
	}
	public String getCompany_input_date() {
		return Company_input_date;
	}
	public void setCompany_input_date(String company_input_date) {
		Company_input_date = company_input_date;
	}
	public String getCompany_output_date() {
		return Company_output_date;
	}
	public void setCompany_output_date(String company_output_date) {
		Company_output_date = company_output_date;
	}
	
	
}
